SOLID is an acronym that represents five principles of object-oriented programming (OOP) intended to 
make software designs more understandable, flexible, and maintainable. These principles 
were introduced by Robert C. Martin (also known as Uncle Bob) in the early 2000s. Here's a 
brief overview of each principle:

i)	Single Responsibility Principle (SRP)
ii)	Open/Closed Principle (OCP)
iii)	Liskov Substitution Principle (LSP)
iv)	Interface Segregation Principle (ISP)
v)	Dependency Inversion Principle (DIP)
