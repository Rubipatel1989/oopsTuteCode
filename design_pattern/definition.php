// Design patterns are widely used solutions to common software design problems. They represent best practices that 
// developers have discovered over time. Design patterns help to create code that is more maintainable, scalable, and 
// understandable. There are several types of design patterns, including:

// 1 Creational Patterns: These patterns deal with object creation mechanisms, trying to create objects in a manner 
//     suitable to the situation. Examples include Singleton, Factory Method, Abstract Factory, Builder, and Prototype.
// 2. Structural Patterns: These patterns deal with object composition and typically identify simple ways to realize 
//     relationships between different objects. Examples include Adapter, Bridge, Composite, Decorator, Facade, Flyweight, and Proxy.
// 3. Behavioral Patterns: These patterns focus on communication between objects, defining how objects interact. 
//     Examples include Chain of Responsibility, Command, Iterator, Mediator, Memento, Observer, State, Strategy, Template Method, and Visitor.
